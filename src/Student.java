public class Student {
    String name, group;
    int age;
    double gpa;
    Student(String name, String group, int age,double gpa){
        this.name = name;
        this.group = group;
        this.age = age;
        this.gpa = gpa;
    }
    public String getName(){
        return name;
    }
    public double getGpa(){
        return getGpa();
    }
}
